export function translate(language, text, fallback) {
  if (!text) return fallback ?? text;
  if (language === "en") return fallback ?? text;
  return TRANSLATIONS[language]?.[text] ?? fallback ?? text;
}

export function translateEnum(language, value) {
  if (value == null) return "";
  const raw = String(value);
  const key = raw.trim().toUpperCase();
  if (language === "en") return raw;
  return ENUM_TRANSLATIONS[language]?.[key] ?? ENUM_TRANSLATIONS[language]?.[key.replaceAll(" ", "_")] ?? raw;
}
